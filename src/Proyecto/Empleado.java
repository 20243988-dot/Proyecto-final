package Proyecto;

/**
 *
 * @author xxjim
 */
public class Empleado {
    protected int dni;
    protected String nombres;
    protected String appellidos;
    protected String usuario;
    protected String password;
    protected String rol;

    public Empleado(int dni, String nombres, String appellidos, String usuario, String password, String rol) {
        this.dni = dni;
        this.nombres = nombres;
        this.appellidos = appellidos;
        this.usuario = usuario;
        this.password = password;
        this.rol = rol;
    }

    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getAppellidos() {
        return appellidos;
    }

    public void setAppellidos(String appellidos) {
        this.appellidos = appellidos;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }
    
    
    
}
